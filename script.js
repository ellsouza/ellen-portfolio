const STORAGE_KEY = "portfolio_language";
const metaDescription = document.querySelector('meta[name="description"]');
const pageTitle = document.querySelector("title");
const langToggle = document.querySelector("#lang-toggle");

const updateTextNodes = (dictionary) => {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = dictionary[key];
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    const value = dictionary[key];
    if (typeof value === "string") {
      element.setAttribute("aria-label", value);
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    const value = dictionary[key];
    if (typeof value === "string") {
      element.setAttribute("alt", value);
    }
  });
};

const applyLanguage = (language) => {
  const lang = translations[language] ? language : "pt";
  const dictionary = translations[lang];

  document.documentElement.lang = dictionary.pageLang;

  if (metaDescription) {
    metaDescription.setAttribute("content", dictionary.metaDescription);
  }

  if (pageTitle) {
    pageTitle.textContent = dictionary.pageTitle;
  }

  updateTextNodes(dictionary);

  if (langToggle) {
    const nextLanguage = lang === "pt" ? "en" : "pt";
    langToggle.textContent = nextLanguage.toUpperCase();
    langToggle.dataset.lang = lang;
    langToggle.setAttribute("aria-label", dictionary.toggleAria);
  }
};

const readStoredLanguage = () => {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    return translations[saved] ? saved : null;
  } catch {
    return null;
  }
};

const saveLanguage = (language) => {
  try {
    localStorage.setItem(STORAGE_KEY, language);
  } catch {
    // Ignore localStorage errors (private mode, blocked storage).
  }
};

const defaultLanguage = () => {
  const browserLanguage = navigator.language ? navigator.language.toLowerCase() : "";
  return browserLanguage.startsWith("pt") ? "pt" : "en";
};

const initialLanguage = readStoredLanguage() || defaultLanguage();
applyLanguage(initialLanguage);

if (langToggle) {
  langToggle.addEventListener("click", () => {
    const currentLanguage = langToggle.dataset.lang || "pt";
    const nextLanguage = currentLanguage === "pt" ? "en" : "pt";
    applyLanguage(nextLanguage);
    saveLanguage(nextLanguage);
  });
}

const glow = document.querySelector(".cursor-glow");
const cards = document.querySelectorAll("[data-tilt]");

if (glow) {
  window.addEventListener("pointermove", (event) => {
    glow.style.transform = `translate3d(${event.clientX - 170}px, ${event.clientY - 170}px, 0)`;
  });
}

const prefersReduced = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

if (!prefersReduced) {
  cards.forEach((card) => {
    card.addEventListener("pointermove", (event) => {
      const rect = card.getBoundingClientRect();
      const px = (event.clientX - rect.left) / rect.width;
      const py = (event.clientY - rect.top) / rect.height;
      const rx = (0.5 - py) * 6;
      const ry = (px - 0.5) * 8;

      card.style.transform = `translateY(-6px) rotateX(${rx.toFixed(2)}deg) rotateY(${ry.toFixed(2)}deg)`;
    });

    card.addEventListener("pointerleave", () => {
      card.style.transform = "";
    });
  });
}
